/*8.1*/
SELECT DWDATE.DAYNAME AS WEEKDAY, DWSALE.QTY*DWSALE.SALEPRICE AS TOTAL_SALES
FROM DWDATE, DWSALE
WHERE DWDATE.DATEKEY = DWSALE.SALE_DWDATEID ORDER BY TOTAL_SALES DESC
FETCH FIRST 7 ROWS ONLY;

/*8.2*/
SELECT DWCUST.CUSTCATNAME AS CUSTCATNAME, DWSALE.QTY*DWSALE.SALEPRICE AS TOTAL_SALES
FROM DWCUST, DWSALE WHERE DWCUST.DWCUSTID = DWSALE.DWCUSTID
ORDER BY TOTAL_SALES ASC;

/*8.3*/
SELECT DWPROD.PRODMANUNAME AS PRODMANUNAME, DWSALE.QTY AS TOTAL_QTY_SOLD
FROM DWPROD, DWSALE WHERE DWPROD.DWPRODID = DWSALE.DWPRODID
ORDER BY DWSALE.QTY DESC;

/*8.4*/
SELECT DWCUST.DWCUSTID, DWCUST.FIRSTNAME, DWCUST.SURNAME, DWSALE.QTY*DWSALE.SALEPRICE AS TOTAL_SALES
FROM DWCUST, DWSALE WHERE DWCUST.DWCUSTID = DWSALE.DWCUSTID
ORDER BY TOTAL_SALES DESC
FETCH FIRST 10 ROWS ONLY;

/*8.5*/
SELECT * FROM (SELECT DWPROD.DWPRODID, DWPROD.PRODNAME, DWSALE.QTY*DWSALE.SALEPRICE AS TOTAL_SALES
FROM DWPROD, DWSALE WHERE DWPROD.DWPRODID=DWSALE.DWPRODID
ORDER BY TOTAL_SALES ASC) estats
WHERE rownum <=10
ORDER BY rownum ASC;

/*8.6*/
SELECT DWCUST.STATE, DWCUST.CITY, DWSALE.SALEPRICE*DWSALE.QTY AS TOTAL_SALES
FROM DWCUST, DWSALE WHERE DWCUST.DWCUSTID=DWSALE.DWCUSTID
ORDER BY STATE ASC;
